/**
 * Compile a pixel grid into an SVG path `d` attribute. Adjacent filled pixels
 * in a row collapse into a single horizontal-run rectangle, which keeps the
 * path string short (matters for lots-of-icons pages).
 */
export function gridToPath(grid: PixelGrid): string {
  const segments: string[] = [];
  for (let y = 0; y < grid.length; y++) {
    const row = grid[y];
    if (!row) continue;
    let runStart = -1;
    for (let x = 0; x <= row.length; x++) {
      const filled = x < row.length && row[x] === '#';
      if (filled && runStart < 0) {
        runStart = x;
      } else if (!filled && runStart >= 0) {
        const w = x - runStart;
        segments.push(`M${runStart} ${y}h${w}v1h-${w}z`);
        runStart = -1;
      }
    }
  }
  return segments.join('');
}
